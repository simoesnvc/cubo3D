char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (*s)
	{
		s++;
		i++;
	}
	while (i > 0 && *s != (char)c)
	{
		i--;
		s--;
	}
	if (*s == (char)c)
		return ((char *)s);
	return (0);
}
